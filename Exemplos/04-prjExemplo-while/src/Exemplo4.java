public class Exemplo4 {
    public static void main(String[] args) {
        
        int cont;
        
        System.out.println("\tTABUADA DO 7\n");
        cont=1;
        
        //ESTRUTURA REPETITIVA WHILE
        while (cont <= 10) {
            System.out.println("7 * " + cont + " = " + cont * 7);
            cont++;
        }
    }
    
}
